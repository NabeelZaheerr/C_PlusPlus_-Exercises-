#include <iostream>
using namespace std;
int main()
{
	int y = 0, z = 0;
	cout << "This Program would perform basic arithmetic Operations on 2 Integers." <<endl;
	cout << "Please Input the first integer." <<endl;
	cin >> y;
	cout << "Please input the value of the 2nd integer." <<endl;
	cin >> z;
	cout << "The Sum of " << y << " and " << z << " is equal to " << y+z << "." <<endl;
	cout << "The Difference of " << y << " and " << z << " is equal to " << y-z << "." <<endl;
	cout << "The Product of " << y << " and " << z << " is equal to " << y*z << "." <<endl;
	cout << "The Division of " << y << " and " << z << " is equal to " << y/z << "." <<endl;
return 0;
}
